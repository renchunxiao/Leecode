public class Solution2 {

    // 4 的幂次，有几个规则。都可以转换成 2 的幂次。
    // 4^0 = 2^0   4^1=2^2   4^2=2^4 看到规律了，4的0-n幂次，就相当于2的0,2,4,6,8...幂次
    // 所以这个题可以先，判断这个数首先是2的幂次，使用(num&(num-1)) == 0 就可判断出来
    // 然后在判断，在2的1,3,5,7...位数上为0，或者在2的0,2,4,6,8...位置上为1
    public boolean isPowerOfFour(int num) {
        return num > 0 && (num&(num-1)) == 0 && (num & 0x55555555) != 0;//判断0,2,4,6...位置上为1
        return num > 0 && (num&(num-1)) == 0 && (num & 0xAAAAAAAA) == 0;//判断1,3,5,7...位置上为0
    }
}
